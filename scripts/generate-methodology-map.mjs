#!/usr/bin/env node
// scripts/generate-methodology-map.mjs
//
// Reads every content/methodology/<slug>.md (excluding files prefixed
// with _) and generates lib/methodology/bodies.generated.ts — a static
// Record<slug, body> that Edge middleware can import to serve raw
// markdown via Accept: text/markdown content negotiation.
//
// Edge middleware can't access the filesystem, so we inline the bodies
// at build time. Run via `npm run prebuild`.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "content", "methodology");
const DEST = path.join(ROOT, "lib", "methodology", "bodies.generated.ts");

const files = fs.existsSync(SRC)
  ? fs
      .readdirSync(SRC)
      .filter((f) => f.endsWith(".md") && !f.startsWith("_"))
      .sort()
  : [];

const entries = {};
for (const file of files) {
  const slug = file.replace(/\.md$/, "");
  const body = fs.readFileSync(path.join(SRC, file), "utf8");
  entries[slug] = body;
}

const banner = `// AUTO-GENERATED by scripts/generate-methodology-map.mjs.
// DO NOT EDIT MANUALLY. Run \`npm run prebuild\` (or \`npm run build\`) to regenerate.
// Source: content/methodology/*.md\n`;

const out = `${banner}
export const METHODOLOGY_BODIES: Record<string, string> = ${JSON.stringify(
  entries,
  null,
  2,
)};

export const METHODOLOGY_SLUGS = Object.keys(METHODOLOGY_BODIES) as readonly string[];
`;

fs.mkdirSync(path.dirname(DEST), { recursive: true });
fs.writeFileSync(DEST, out);
console.log(
  `[methodology] generated ${path.relative(ROOT, DEST)} with ${
    files.length
  } article${files.length === 1 ? "" : "s"}`,
);
